# MATHEMATICAL OPERATIONS

# Python already contains all the basic math operations

print(3 + 5)    # addition
print(7 - 4)    # subtraction
print(3 * 2)    # multiplication
print(6 / 3)    # division (this will ALWAYS result in a float type)
print(2**3)     # exponential

# These ALWAYS follow PEMDAS: (), **, *, /, +, /
# For operations which are hierarchically the same, it goes in a left-to-right order

print(3 * 3 + 3 / 3 - 3)

print((3*(3+3))/3 - 3)
