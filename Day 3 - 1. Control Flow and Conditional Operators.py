# IF-ELSE STATEMENTS AND CONDITIONAL OPERATIONS

# Using if-else statements is a way to divide data into categories based on its attributes
print("Welcome to the roller coaster!")
height = int(input("What is your height in cm? "))
if height >= 120:
    print("You can ride the roller coaster!")     # The indentation says that this line acts underneath the above one
else:
    print("Sorry, you have to grow taller before you can ride.")

