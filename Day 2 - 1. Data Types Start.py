# DATA TYPES

# Strings
print("Hello"[0])
# When you want to pull out a specific part of a string, you can use the square brackets
print("Hello"[4])
# Remember though that numbers in the quotes are still treated as strings, not actual numbers
print("123" + "456")
# The same thing happens as if we tried to concatenate actual words
print("Hello " + "World!")

# Integers
print(123 + 456)    # This should give us an output of 579

# Floats
print(3.14159)  # Floats are all numbers with decimal points

# Booleans
print(True)
print(False)
# Just these 2 data types: true and false
